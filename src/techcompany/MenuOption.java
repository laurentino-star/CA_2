package techcompany; // Declares that this class is part of the techcompany package

// Enum type to represent the different menu options available in the program
public enum MenuOption {
    SORT,             // Option to sort the employee list
    SEARCH,           // Option to search for an employee
    ADD_RECORDS,      // Option to manually add a new employee record
    GENERATE_RANDOM,  // Option to generate random employee records
    EXIT              // Option to exit the program
}
